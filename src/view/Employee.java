    private void EEmployeeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EEmployeeBtnActionPerformed
        if("".equals(NameText.getText())&&"".equals(IdText.getText())){
               JOptionPane.showMessageDialog(null, "Cần chọn nhân viên để chỉnh sửa");
           }else if("".equals(PhoneText.getText())){
               JOptionPane.showMessageDialog(null, "Cần chọn nhân viên để chỉnh sửa"); 
           }else if(containsSpecialCharacter(NameText.getText())&&containsSpecialCharacter(PhoneText.getText())&&containsSpecialCharacter(AddressText.getText())){
                JOptionPane.showMessageDialog(null, "Các dòng không thể chứa kí tự đặc biệt");
            }else if(!isNumeric(PhoneText.getText())){
               JOptionPane.showMessageDialog(null, "SDT không thể chứa chữ và kí tự đặc biệt"); 
           }else{
                String address=AddressText.getText();
                int id=Integer.parseInt(IdText.getText());
                String namee =NameText.getText().trim();
                String phonee= PhoneText.getText().trim();
                int workt=Integer.parseInt(HouseWorkText.getText());
                EController e= new EController();
                e.update(id, namee, phonee, address,workt);
                reset();
                refreshTable();
                EEmployeeBtn.setSelected(false);
           }
        
    }//GEN-LAST:event_EEmployeeBtnActionPerformed

