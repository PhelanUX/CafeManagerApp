    private void importDataToJTable() {
    try {
        //  Kết nối đến cơ sở dữ liệu
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafemanager", "root", "");

        //  Thực thi câu truy vấn SQL
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM employee");

        //  Đưa dữ liệu vào DefaultTableModel
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Tên", "Điện thoại","Địa chỉ","Số giờ làm"}, 0);
        while(rs.next()) {
            String col2 = rs.getString("name");
            String col3 = rs.getString("phone");
            String col1 = rs.getString("ID");
            String col4=rs.getString("address");
            int col5=rs.getInt("sogiolam");
            model.addRow(new Object[]{col1, col2, col3,col4,col5});
        }

        //  Đặt DefaultTableModel này cho JTable
        jTable1.setModel(model);

    } catch (Exception ex) {
        ex.printStackTrace();
        }
    }
       
