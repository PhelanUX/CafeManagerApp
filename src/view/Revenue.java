    private void setTime(){
        
        new Thread(new Runnable(){
            @Override
            public void run(){
                while(true){
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        java.util.logging.Logger.getLogger(Revenue.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
                     Date date = new Date();
                    SimpleDateFormat tf = new SimpleDateFormat("HH:mm:ss aa");
                    SimpleDateFormat df = new SimpleDateFormat("EEEE, dd-MM-yy");
                    
                    String time = tf.format(date);
                    jTextTime2.setText(time.split(" ")[0] + " " + time.split(" ")[1]);
                    jTextDate2.setText(df.format(date));
                    
                }
            }           
        }).start();
    }
