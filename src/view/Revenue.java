    private void setCurrentMonth(){
        int currentMonth = LocalDate.now().getMonthValue();
        int currentYear = LocalDate.now().getYear();
        RevenueController rc= new RevenueController();
                DefaultCategoryDataset dataset = rc.getRevenueDatasetCurrent();
                JFreeChart barChart = ChartFactory.createBarChart(
                "Doanh thu theo ngày (" + currentMonth + "/" + currentYear + ")", // Tiêu đề biểu đồ
                "Ngày", // Tiêu đề trục X
                "Doanh thu (VNĐ)", // Tiêu đề trục Y
                dataset // Dữ liệu
                );
                CategoryPlot plot = (CategoryPlot) barChart.getPlot();
                BarRenderer renderer = (BarRenderer) plot.getRenderer();

                // Hiển thị giá trị trên mỗi cột
                renderer.setDefaultItemLabelsVisible(true); // Hiển thị nhãn
                renderer.setDefaultItemLabelGenerator(new StandardCategoryItemLabelGenerator());
                renderer.setDefaultItemLabelFont(new Font("SansSerif", Font.BOLD, 12));
                renderer.setDefaultItemLabelPaint(Color.BLACK);
                 ChartPanel chartPanel = new ChartPanel(barChart);
                chartPanel.setPreferredSize(new Dimension(600,400)); // Kích thước của biểu đồ
               
                jPanel2.setLayout(new BorderLayout()); // Đặt layout nếu chưa có
                jPanel2.add(chartPanel, BorderLayout.CENTER); // 
    }
